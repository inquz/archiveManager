/**
 * @file input.h
 * @brief Модуль добавления новых жильцов в архив
 *
 * Данный заголовочный файл предоставляет функции для интерактивного добавления
 * новых записей о жильцах в архив в различных форматах.
 */

#pragma once
#include "base.h"

 /**
  * @brief Добавляет нового жильца в формате input.txt
  *
  * Интерактивно запрашивает у пользователя все необходимые данные для полной
  * записи о жильце и добавляет их в стек. Включает все поля структуры Tenant.
  *
  * @param stack Указатель на стек, куда будет добавлена новая запись
  *
  * @note Запрашиваемые данные:
  * - Номер карточки
  * - Адрес проживания
  * - ФИО жильца
  * - Количество проживающих
  * - Дата заселения (в формате DD.MM.YYYY)
  * - Общая площадь помещения
  * - Избыточная площадь
  *
  * @warning Проверяет корректность вводимых данных (особенно числовых значений)
  */
void addTenantToInput(Stack* stack);

/**
 * @brief Добавляет нового жильца в формате kodif.txt
 *
 * Интерактивно запрашивает у пользователя ограниченный набор данных
 * для сокращенной записи о жильце и добавляет их в стек.
 *
 * @param stack Указатель на стек, куда будет добавлена новая запись
 *
 * @note Запрашиваемые данные:
 * - Номер карточки
 * - Адрес проживания
 * - ФИО жильца
 * - Количество проживающих
 *
 * @warning Проверяет уникальность номера карточки в архиве
 */
void addTenantToKodif(Stack* stack);