/**
 * @file ul.h
 * @brief Модуль утилит вывода информации
 *
 * Содержит вспомогательные функции для форматированного вывода сообщений,
 * ошибок и данных в консольном интерфейсе.
 */

#pragma once
#include "base.h"

 /**
  * @brief Выводит сообщение об ошибке
  *
  * Отображает переданное сообщение в стандартном формате ошибки
  * (обычно с красным цветом текста и префиксом "ОШИБКА:")
  *
  * @param message Текст сообщения об ошибке
  */
void printError(const char* message);

/**
 * @brief Выводит сообщение об успехе
 *
 * Отображает переданное сообщение в стандартном формате успешного выполнения
 * (обычно с зеленым цветом текста и префиксом "УСПЕХ:")
 *
 * @param message Текст сообщения об успехе
 */
void printSuccess(const char* message);

/**
 * @brief Выводит текст с выравниванием по центру
 *
 * Форматирует и выводит переданную строку текста, центрируя ее
 * в указанной ширине поля
 *
 * @param text Текст для вывода
 * @param width Ширина поля для центрирования
 */
void printCentered(const char* text, int width);

/**
 * @brief Выводит целое число с выравниванием по центру
 *
 * Форматирует и выводит целое число, центрируя его
 * в указанной ширине поля
 *
 * @param value Числовое значение для вывода
 * @param width Ширина поля для центрирования
 */
void printCenteredInt(int value, int width);

/**
 * @brief Выводит дробное число с выравниванием по центру
 *
 * Форматирует и выводит дробное число с заданной точностью,
 * центрируя его в указанной ширине поля
 *
 * @param value Числовое значение для вывода
 * @param width Ширина поля для центрирования
 * @param precision Количество знаков после запятой
 */
void printCenteredDouble(double value, int width, int precision);